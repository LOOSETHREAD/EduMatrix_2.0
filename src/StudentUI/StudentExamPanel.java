    public void addItem(ModelExam data) {
        StudentExamBox exam = new StudentExamBox();
        exam.setData(data);
        exam.studentId = Student.data.getStudentid();
        
        exam.onClick(event); // Attach the event handler
        panelItem1.add(exam);
        refreshPanel();
    }
    private void refreshPanel() {
        repaint();
        revalidate();
    }
    private void loadQuizzes() {
        setEvent(new EventItem3() {
            @Override
            public void itemClick3(Component com, ModelExam data) {
                // Handle quiz click event
            }
        });

        List<ModelExam> exams = PopulateTable.populateExamNameToExamBox(courseCode);

        panelItem1.removeAll();

        for (ModelExam exam : exams) {
            addItem(exam);
        }

        refreshPanel();
    }
